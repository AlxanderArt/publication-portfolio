export function getPublicationBySlug(slug: string): Publication | undefined {
  return publications.find((p) => p.slug === slug);
}

export function getPublicationsByCategory(category: string): Publication[] {
  if (category === "All") return publications;
  return publications.filter((p) => p.category === category);
}

export function getAllCategories(): string[] {
  const cats = new Set(publications.map((p) => p.category));
  return ["All", ...Array.from(cats)];
}
